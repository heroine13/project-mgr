<template>
  <router-view />
</template>

<script setup lang="ts">
import { onMounted } from 'vue'
import { useI18n } from 'vue-i18n'

const { locale } = useI18n()

// 初始化语言设置
onMounted(() => {
  const savedLanguage = localStorage.getItem('user_language')
  if (savedLanguage) {
    locale.value = savedLanguage
  }
})
</script>

